export const GENERATED_PR_POLICY_MARKER = "<!-- aster:generated-pr-policy";

export function buildGeneratedPrPolicyMarker({ lane, mergePolicy = "human_review", draftOnly = true }) {
  return `${GENERATED_PR_POLICY_MARKER} lane=${lane} merge=${mergePolicy} draft_only=${draftOnly ? "true" : "false"} -->`;
}

export function parseGeneratedPrPolicy(body) {
  const match = String(body ?? "").match(/<!-- aster:generated-pr-policy\s+lane=([^\s]+)\s+merge=([^\s]+)\s+draft_only=(true|false)\s*-->/);
  if (!match) {
    return null;
  }
  return {
    lane: match[1],
    merge_policy: match[2],
    draft_only: match[3] === "true",
  };
}

export function inferGeneratedPrLane({ headRefName, title, body }) {
  const parsed = parseGeneratedPrPolicy(body);
  if (parsed?.lane) {
    return parsed.lane;
  }
  const branch = String(headRefName ?? "").trim().toLowerCase();
  const normalizedTitle = String(title ?? "").trim().toLowerCase();
  if (branch.startsWith("runx/operator-memory-issue-triage-") || normalizedTitle.includes("issue-triage operator memory")) {
    return "issue-triage";
  }
  if (branch.startsWith("runx/skill-") || normalizedTitle.includes("skill proposal")) {
    return "skill-lab";
  }
  if (branch.startsWith("runx/add-skill-md") || normalizedTitle.includes("portable skill.md")) {
    return "skill-upstream";
  }
  return "unknown";
}

export function ensureGeneratedPrPolicyBlock(body, { lane, changeSurfacePolicy = null }) {
  const trimmed = String(body ?? "").trim();
  if (parseGeneratedPrPolicy(trimmed)) {
    return trimmed;
  }
  const marker = buildGeneratedPrPolicyMarker({ lane });
  const policyBlock = [
    marker,
    "## Generated PR Policy",
    "",
    `- Generated by lane: \`${lane}\``,
    "- Publication mode: `draft-only` until a human intentionally promotes the PR",
    "- Merge policy: `human-reviewed` only; no autonomous merge is allowed",
    "- Correction policy: use the `rollback` workflow or a corrective PR/comment when this output is wrong",
    "- Receipts: inspect the linked workflow artifacts before review or merge",
  ].join("\n");
  const surfaceBlock = changeSurfacePolicy
    ? [
        "## Change Surface Policy",
        "",
        `- Repo scope: ${changeSurfacePolicy.internal_repo ? "`aster` parity enforced" : "`external` report-only"}`,
        `- Policy status: \`${changeSurfacePolicy.status}\``,
        `- Surfaces touched: ${(changeSurfacePolicy.surfaces ?? []).map((surface) => `\`${surface}\``).join(", ") || "`none`"}`,
        ...(Array.isArray(changeSurfacePolicy.reasons) && changeSurfacePolicy.reasons.length > 0
          ? [`- Reasons: ${changeSurfacePolicy.reasons.map((reason) => `\`${reason}\``).join(", ")}`]
          : []),
      ].join("\n")
    : "";
  return [trimmed, policyBlock, surfaceBlock].filter(Boolean).join("\n\n").trim();
}

export function buildGeneratedPrPolicyPlan(report) {
  const headRefName = String(report?.headRefName ?? "").trim();
  if (!headRefName.startsWith("runx/")) {
    return {
      status: "noop",
      reason: "not_generated_branch",
    };
  }

  const lane = inferGeneratedPrLane(report);
  const nextBody = ensureGeneratedPrPolicyBlock(report.body ?? "", { lane });
  const actions = [];
  if (nextBody !== String(report.body ?? "").trim()) {
    actions.push("patch_body");
  }
  if (report.isDraft === false) {
    actions.push("convert_to_draft");
  }

  return {
    status: actions.length > 0 ? "enforce" : "compliant",
    lane,
    actions,
    next_body: nextBody,
  };
}
